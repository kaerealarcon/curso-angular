let a = 1;
let b = 2;

console.log(a);
// retorna 1
console.log(a + b);
// retorna 3
console.log(1 + b);
// retorna 3

a += 1;
console.log(a);
// retorna 2, é o mesmo que (a = a + 1) = 2

a -= 1;
console.log(a);
// retorna 0, é o mesmo que (a = a - 1) = 0

a *= 2;
console.log(a);
// retorna 2, é o mesmo que (a = a * 2) = 0

a /= 1;
console.log(a);
// retorna 1, é o mesmo que (a = a / 1) = 1
